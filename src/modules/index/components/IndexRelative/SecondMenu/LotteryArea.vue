  function getByClass(oParent,nClass)
  {
    var eLe = oParent.getElementsByTagName('*');
    var aRrent  = [];
    for(var i=0; i<eLe.length; i++)
    {
      if(eLe[i].className == nClass)
      {
        aRrent.push(eLe[i]);
      }
    }
